package basics.ch_01_introduction;

public class PrintLineMethodDemo {

	public static void main(String[] args) {
		// println() // print + ln
		// this will create a new line after printing
		System.out.println("Hello World");

		// \ backslash is called a escape character in java
		System.out.println(" \" ");
		System.out.println(" \\ ");
		System.out.println("We are learning \nJava");
		System.out.println(" ' ");
	}
}
