package basics.ch_03_variables;

public class VariableDemo1 {

	public static void main(String[] args) {
		// 1st number = 3
		// 2nd number = 6
		int a = 3;
		int b = 6;

		// SYNATX:
		// datatype variableName = value;

		// addition
		System.out.println(a + b);

		// subtraction
		System.out.println(b - a);

		// multiplication
		System.out.println(a * b);

		// division
		System.out.println(b / a);

	}

}
